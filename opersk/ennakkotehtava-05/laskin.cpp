#include "laskin.hh"

using namespace std;

double yhteen(double summattava, double summaaja)
{
    return summattava + summaaja;
}

double vahennys(double vahennettava, double vahentaja)
{
    return vahennettava - vahentaja;
}

double kerto(double kerrottava, double kertoja)
{
    return kerrottava * kertoja;
}

double jako(double jaettava, double jakaja)
{
    return jaettava / jakaja;
}
